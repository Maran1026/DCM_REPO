-- Access control: database roles, warehouse role, and grants
DEFINE DATABASE ROLE GITOPS_PROJECT_PROD.ADMIN
    COMMENT = 'Full control: DDL + DML - PROD';

DEFINE DATABASE ROLE GITOPS_PROJECT_PROD.DEVELOPER
    COMMENT = 'Read/write: DML only - PROD';

DEFINE DATABASE ROLE GITOPS_PROJECT_PROD.ANALYST
    COMMENT = 'Read-only: SELECT only - PROD';

DEFINE ROLE GITOPS_WH_USER_PROD
    COMMENT = 'Warehouse access for GitOps project - PROD';

GRANT DATABASE ROLE GITOPS_PROJECT_PROD.ANALYST TO DATABASE ROLE GITOPS_PROJECT_PROD.DEVELOPER;
GRANT DATABASE ROLE GITOPS_PROJECT_PROD.DEVELOPER TO DATABASE ROLE GITOPS_PROJECT_PROD.ADMIN;
GRANT DATABASE ROLE GITOPS_PROJECT_PROD.ADMIN TO ROLE SYSADMIN;
GRANT ROLE GITOPS_WH_USER_PROD TO ROLE SYSADMIN;

GRANT USAGE ON WAREHOUSE GITOPS_WH_PROD TO ROLE GITOPS_WH_USER_PROD;

GRANT USAGE ON DATABASE GITOPS_PROJECT_PROD TO DATABASE ROLE GITOPS_PROJECT_PROD.ANALYST;

GRANT USAGE ON SCHEMA GITOPS_PROJECT_PROD.RAW TO DATABASE ROLE GITOPS_PROJECT_PROD.ANALYST;
GRANT USAGE ON SCHEMA GITOPS_PROJECT_PROD.ANALYTICS TO DATABASE ROLE GITOPS_PROJECT_PROD.ANALYST;
GRANT USAGE ON SCHEMA GITOPS_PROJECT_PROD.SERVE TO DATABASE ROLE GITOPS_PROJECT_PROD.ANALYST;

GRANT SELECT ON ALL TABLES IN SCHEMA GITOPS_PROJECT_PROD.RAW TO DATABASE ROLE GITOPS_PROJECT_PROD.ANALYST;
GRANT SELECT ON ALL VIEWS IN SCHEMA GITOPS_PROJECT_PROD.SERVE TO DATABASE ROLE GITOPS_PROJECT_PROD.ANALYST;
GRANT SELECT ON ALL DYNAMIC TABLES IN SCHEMA GITOPS_PROJECT_PROD.ANALYTICS TO DATABASE ROLE GITOPS_PROJECT_PROD.ANALYST;

GRANT INSERT, UPDATE, DELETE ON ALL TABLES IN SCHEMA GITOPS_PROJECT_PROD.RAW TO DATABASE ROLE GITOPS_PROJECT_PROD.DEVELOPER;

GRANT CREATE TABLE, CREATE VIEW, CREATE DYNAMIC TABLE
    ON SCHEMA GITOPS_PROJECT_PROD.RAW TO DATABASE ROLE GITOPS_PROJECT_PROD.ADMIN;
GRANT CREATE TABLE, CREATE VIEW, CREATE DYNAMIC TABLE
    ON SCHEMA GITOPS_PROJECT_PROD.ANALYTICS TO DATABASE ROLE GITOPS_PROJECT_PROD.ADMIN;
GRANT CREATE TABLE, CREATE VIEW, CREATE DYNAMIC TABLE
    ON SCHEMA GITOPS_PROJECT_PROD.SERVE TO DATABASE ROLE GITOPS_PROJECT_PROD.ADMIN;