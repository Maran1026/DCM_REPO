DEFINE DYNAMIC TABLE GITOPS_PROJECT_PROD.ANALYTICS.DT_CUSTOMER_ORDERS
WAREHOUSE = 'GITOPS_WH_PROD'
TARGET_LAG = '10 minutes'
INITIALIZE = 'ON_CREATE'
AS
SELECT
    c.CUSTOMER_ID,
    c.FIRST_NAME,
    c.LAST_NAME,
    c.EMAIL,
    c.REGION,
    COUNT(DISTINCT o.ORDER_ID) AS TOTAL_ORDERS,
    SUM(o.TOTAL_AMOUNT) AS TOTAL_SPEND,
    MIN(o.ORDER_DATE) AS FIRST_ORDER_DATE,
    MAX(o.ORDER_DATE) AS LAST_ORDER_DATE,
    CASE WHEN COUNT(DISTINCT o.ORDER_ID) > 0 THEN SUM(o.TOTAL_AMOUNT) / COUNT(DISTINCT o.ORDER_ID) ELSE 0 END AS AVG_ORDER_VALUE,
    DATEDIFF('DAY', MAX(o.ORDER_DATE), CURRENT_DATE()) AS DAYS_SINCE_LAST_ORDER,
    c.LOYALTY_TIER
FROM GITOPS_PROJECT_PROD.RAW.CUSTOMERS c
LEFT JOIN GITOPS_PROJECT_PROD.RAW.ORDERS o ON c.CUSTOMER_ID = o.CUSTOMER_ID
GROUP BY c.CUSTOMER_ID, c.FIRST_NAME, c.LAST_NAME, c.EMAIL, c.REGION, c.LOYALTY_TIER;

DEFINE DYNAMIC TABLE GITOPS_PROJECT_PROD.ANALYTICS.DT_DAILY_REVENUE
WAREHOUSE = 'GITOPS_WH_PROD'
TARGET_LAG = '10 minutes'
INITIALIZE = 'ON_CREATE'
AS
SELECT
    DATE_TRUNC('DAY', o.ORDER_DATE) AS ORDER_DAY,
    p.CATEGORY,
    COUNT(DISTINCT o.ORDER_ID) AS ORDER_COUNT,
    SUM(o.TOTAL_AMOUNT) AS DAILY_REVENUE,
    CASE WHEN COUNT(DISTINCT o.ORDER_ID) > 0 THEN SUM(o.TOTAL_AMOUNT) / COUNT(DISTINCT o.ORDER_ID) ELSE 0 END AS AVG_ORDER_VALUE,
    MAX(o.TOTAL_AMOUNT) AS MAX_ORDER_AMOUNT
FROM GITOPS_PROJECT_PROD.RAW.ORDERS o
LEFT JOIN GITOPS_PROJECT_PROD.RAW.PRODUCTS p ON o.CUSTOMER_ID = p.PRODUCT_ID
WHERE o.STATUS != 'CANCELLED'
GROUP BY ORDER_DAY, p.CATEGORY;

DEFINE DYNAMIC TABLE GITOPS_PROJECT_PROD.ANALYTICS.DT_ORDER_ITEM_SUMMARY
WAREHOUSE = 'GITOPS_WH_PROD'
TARGET_LAG = '10 minutes'
INITIALIZE = 'ON_CREATE'
AS
SELECT
    p.CATEGORY,
    p.PRODUCT_NAME,
    oi.PRODUCT_ID,
    COUNT(DISTINCT oi.ORDER_ITEM_ID) AS TOTAL_LINE_ITEMS,
    COUNT(DISTINCT oi.ORDER_ID) AS DISTINCT_ORDERS,
    SUM(oi.QUANTITY) AS TOTAL_QUANTITY_SOLD,
    SUM(oi.LINE_TOTAL) AS TOTAL_REVENUE,
    CASE WHEN SUM(oi.QUANTITY) > 0 THEN SUM(oi.LINE_TOTAL) / SUM(oi.QUANTITY) ELSE 0 END AS AVG_SELLING_PRICE
FROM GITOPS_PROJECT_PROD.RAW.ORDER_ITEMS oi
LEFT JOIN GITOPS_PROJECT_PROD.RAW.PRODUCTS p ON oi.PRODUCT_ID = p.PRODUCT_ID
GROUP BY p.CATEGORY, p.PRODUCT_NAME, oi.PRODUCT_ID;