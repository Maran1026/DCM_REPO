-- Access control: database roles, warehouse role, and grants
DEFINE DATABASE ROLE GITOPS_PROJECT_DEV.ADMIN
    COMMENT = 'Full control: DDL + DML - DEV';

DEFINE DATABASE ROLE GITOPS_PROJECT_DEV.DEVELOPER
    COMMENT = 'Read/write: DML only - DEV';

DEFINE DATABASE ROLE GITOPS_PROJECT_DEV.ANALYST
    COMMENT = 'Read-only: SELECT only - DEV';

DEFINE ROLE GITOPS_WH_USER_DEV
    COMMENT = 'Warehouse access for GitOps project - DEV';

GRANT DATABASE ROLE GITOPS_PROJECT_DEV.ANALYST TO DATABASE ROLE GITOPS_PROJECT_DEV.DEVELOPER;
GRANT DATABASE ROLE GITOPS_PROJECT_DEV.DEVELOPER TO DATABASE ROLE GITOPS_PROJECT_DEV.ADMIN;
GRANT DATABASE ROLE GITOPS_PROJECT_DEV.ADMIN TO ROLE SYSADMIN;
GRANT ROLE GITOPS_WH_USER_DEV TO ROLE SYSADMIN;

GRANT USAGE ON WAREHOUSE GITOPS_WH_DEV TO ROLE GITOPS_WH_USER_DEV;

GRANT USAGE ON DATABASE GITOPS_PROJECT_DEV TO DATABASE ROLE GITOPS_PROJECT_DEV.ANALYST;

GRANT USAGE ON SCHEMA GITOPS_PROJECT_DEV.RAW TO DATABASE ROLE GITOPS_PROJECT_DEV.ANALYST;
GRANT USAGE ON SCHEMA GITOPS_PROJECT_DEV.ANALYTICS TO DATABASE ROLE GITOPS_PROJECT_DEV.ANALYST;
GRANT USAGE ON SCHEMA GITOPS_PROJECT_DEV.SERVE TO DATABASE ROLE GITOPS_PROJECT_DEV.ANALYST;

GRANT SELECT ON ALL TABLES IN SCHEMA GITOPS_PROJECT_DEV.RAW TO DATABASE ROLE GITOPS_PROJECT_DEV.ANALYST;
GRANT SELECT ON ALL VIEWS IN SCHEMA GITOPS_PROJECT_DEV.SERVE TO DATABASE ROLE GITOPS_PROJECT_DEV.ANALYST;
GRANT SELECT ON ALL DYNAMIC TABLES IN SCHEMA GITOPS_PROJECT_DEV.ANALYTICS TO DATABASE ROLE GITOPS_PROJECT_DEV.ANALYST;

GRANT INSERT, UPDATE, DELETE ON ALL TABLES IN SCHEMA GITOPS_PROJECT_DEV.RAW TO DATABASE ROLE GITOPS_PROJECT_DEV.DEVELOPER;

GRANT CREATE TABLE, CREATE VIEW, CREATE DYNAMIC TABLE
    ON SCHEMA GITOPS_PROJECT_DEV.RAW TO DATABASE ROLE GITOPS_PROJECT_DEV.ADMIN;
GRANT CREATE TABLE, CREATE VIEW, CREATE DYNAMIC TABLE
    ON SCHEMA GITOPS_PROJECT_DEV.ANALYTICS TO DATABASE ROLE GITOPS_PROJECT_DEV.ADMIN;
GRANT CREATE TABLE, CREATE VIEW, CREATE DYNAMIC TABLE
    ON SCHEMA GITOPS_PROJECT_DEV.SERVE TO DATABASE ROLE GITOPS_PROJECT_DEV.ADMIN;