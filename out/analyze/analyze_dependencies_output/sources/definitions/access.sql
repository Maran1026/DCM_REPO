-- Access control: database roles, warehouse role, and grants
DEFINE DATABASE ROLE GITOPS_PROJECT_TEST.ADMIN
    COMMENT = 'Full control: DDL + DML - TEST';

DEFINE DATABASE ROLE GITOPS_PROJECT_TEST.DEVELOPER
    COMMENT = 'Read/write: DML only - TEST';

DEFINE DATABASE ROLE GITOPS_PROJECT_TEST.ANALYST
    COMMENT = 'Read-only: SELECT only - TEST';

DEFINE ROLE GITOPS_WH_USER_TEST
    COMMENT = 'Warehouse access for GitOps project - TEST';

GRANT DATABASE ROLE GITOPS_PROJECT_TEST.ANALYST TO DATABASE ROLE GITOPS_PROJECT_TEST.DEVELOPER;
GRANT DATABASE ROLE GITOPS_PROJECT_TEST.DEVELOPER TO DATABASE ROLE GITOPS_PROJECT_TEST.ADMIN;
GRANT DATABASE ROLE GITOPS_PROJECT_TEST.ADMIN TO ROLE SYSADMIN;
GRANT ROLE GITOPS_WH_USER_TEST TO ROLE SYSADMIN;

GRANT USAGE ON WAREHOUSE GITOPS_WH_TEST TO ROLE GITOPS_WH_USER_TEST;

GRANT USAGE ON DATABASE GITOPS_PROJECT_TEST TO DATABASE ROLE GITOPS_PROJECT_TEST.ANALYST;

GRANT USAGE ON SCHEMA GITOPS_PROJECT_TEST.RAW TO DATABASE ROLE GITOPS_PROJECT_TEST.ANALYST;
GRANT USAGE ON SCHEMA GITOPS_PROJECT_TEST.ANALYTICS TO DATABASE ROLE GITOPS_PROJECT_TEST.ANALYST;
GRANT USAGE ON SCHEMA GITOPS_PROJECT_TEST.SERVE TO DATABASE ROLE GITOPS_PROJECT_TEST.ANALYST;

GRANT SELECT ON ALL TABLES IN SCHEMA GITOPS_PROJECT_TEST.RAW TO DATABASE ROLE GITOPS_PROJECT_TEST.ANALYST;
GRANT SELECT ON ALL VIEWS IN SCHEMA GITOPS_PROJECT_TEST.SERVE TO DATABASE ROLE GITOPS_PROJECT_TEST.ANALYST;
GRANT SELECT ON ALL DYNAMIC TABLES IN SCHEMA GITOPS_PROJECT_TEST.ANALYTICS TO DATABASE ROLE GITOPS_PROJECT_TEST.ANALYST;

GRANT INSERT, UPDATE, DELETE ON ALL TABLES IN SCHEMA GITOPS_PROJECT_TEST.RAW TO DATABASE ROLE GITOPS_PROJECT_TEST.DEVELOPER;

GRANT CREATE TABLE, CREATE VIEW, CREATE DYNAMIC TABLE
    ON SCHEMA GITOPS_PROJECT_TEST.RAW TO DATABASE ROLE GITOPS_PROJECT_TEST.ADMIN;
GRANT CREATE TABLE, CREATE VIEW, CREATE DYNAMIC TABLE
    ON SCHEMA GITOPS_PROJECT_TEST.ANALYTICS TO DATABASE ROLE GITOPS_PROJECT_TEST.ADMIN;
GRANT CREATE TABLE, CREATE VIEW, CREATE DYNAMIC TABLE
    ON SCHEMA GITOPS_PROJECT_TEST.SERVE TO DATABASE ROLE GITOPS_PROJECT_TEST.ADMIN;