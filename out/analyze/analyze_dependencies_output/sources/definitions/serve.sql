DEFINE VIEW GITOPS_PROJECT_DEV.SERVE.V_CUSTOMER_360
AS
SELECT
    CUSTOMER_ID,
    FIRST_NAME,
    LAST_NAME,
    EMAIL,
    REGION,
    TOTAL_ORDERS,
    TOTAL_SPEND,
    FIRST_ORDER_DATE,
    LAST_ORDER_DATE,
    AVG_ORDER_VALUE,
    DAYS_SINCE_LAST_ORDER,
    LOYALTY_TIER
FROM GITOPS_PROJECT_DEV.ANALYTICS.DT_CUSTOMER_ORDERS
WHERE TOTAL_ORDERS > 0;

DEFINE VIEW GITOPS_PROJECT_DEV.SERVE.V_DAILY_REVENUE_SUMMARY
DATA_METRIC_SCHEDULE = 'USING CRON 0 7 * * * UTC'
AS
SELECT
    ORDER_DAY,
    CATEGORY,
    ORDER_COUNT,
    DAILY_REVENUE,
    AVG_ORDER_VALUE
FROM GITOPS_PROJECT_DEV.ANALYTICS.DT_DAILY_REVENUE
WHERE ORDER_DAY >= DATEADD('DAY', -30, CURRENT_DATE());

DEFINE VIEW GITOPS_PROJECT_DEV.SERVE.V_ACTIVE_PRODUCTS
AS
SELECT
    PRODUCT_ID,
    PRODUCT_NAME,
    CATEGORY,
    UNIT_PRICE,
    SKU,
    STOCK_QUANTITY
FROM GITOPS_PROJECT_DEV.RAW.PRODUCTS
WHERE IS_ACTIVE = TRUE;

DEFINE VIEW GITOPS_PROJECT_DEV.SERVE.V_ORDER_ITEM_DETAILS
AS
SELECT
    oi.PRODUCT_ID,
    s.PRODUCT_NAME,
    s.CATEGORY,
    s.TOTAL_LINE_ITEMS,
    s.DISTINCT_ORDERS,
    s.TOTAL_QUANTITY_SOLD,
    s.TOTAL_REVENUE,
    s.AVG_SELLING_PRICE
FROM GITOPS_PROJECT_DEV.ANALYTICS.DT_ORDER_ITEM_SUMMARY s
JOIN GITOPS_PROJECT_DEV.RAW.ORDER_ITEMS oi ON s.PRODUCT_ID = oi.PRODUCT_ID
WHERE s.TOTAL_QUANTITY_SOLD > 0;